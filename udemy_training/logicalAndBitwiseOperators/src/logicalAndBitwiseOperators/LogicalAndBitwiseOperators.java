package logicalAndBitwiseOperators;


public class LogicalAndBitwiseOperators {

	public static void main(String[] args) {
		
		/*
		 * 
		 *  Logical AND - &&
		 *  
		 *  Logical OR - ||
		 *  
		 *  Logical Not - !
		 *  
		 *  Bitwise AND - &
		 *  
		 *  Bitwise OR - |
		 *  
		 *  Bitwise compliment- ~
		 *  
		 *  
		 *  
		 *  
		 */
		
		int num1 = 60;// 111100
		int num2 = 42;// 101010
		              // 101000 => 40
		              // 111110 => 62
		
	    System.out.println(num1 | num2);

	}

}